export const DECK_SIZE = 6;
export const MAX_BENCH = 2;
export const MAX_ENERGON = 5;
export const STARTING_HAND = 3;

export function getCard(id) {
  return CARDS.find((c) => c.id === id);
}

export function cardsByFaction(faction) {
  return CARDS.filter((c) => c.faction === faction);
}

export function defaultDeck(faction) {
  const pool =
    faction === 'Maximal'
      ? CARDS.filter((c) => c.faction === 'Maximal')
      : CARDS.filter((c) => c.faction === 'Predacon' || c.faction === 'Other');
  // Prefer balanced costs
  return [...pool]
    .sort((a, b) => a.cost - b.cost || b.rarity.localeCompare(a.rarity))
    .slice(0, DECK_SIZE)
    .map((c) => c.id);
}

export function aiDeck() {
  // AI plays Predacons
  const preds = CARDS.filter((c) => c.faction === 'Predacon');
  const picks = [...preds].sort(() => Math.random() - 0.5).slice(0, DECK_SIZE);
  // Always try to include Megatron if available
  if (!picks.some((c) => c.id === 11)) {
    picks[picks.length - 1] = CARDS.find((c) => c.id === 11);
  }
  return picks.map((c) => c.id);
}
